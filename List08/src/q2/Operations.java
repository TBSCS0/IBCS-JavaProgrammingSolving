package q2;

public class Operations {


    // Methods
    Operations(){
    }

    int add(int x, int y){
        return x+y;
    }

    int sub(int x, int y){
        return x-y;
    }

    int mult(int x, int y){
        return x*y;
    }

    double div(int x, int y){
        double z = x/y;
        return z;
    }
}
